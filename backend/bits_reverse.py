import temp

def test_reverse_bits():
    assert temp.reverse_bits(0b00000010100101000001111010011100) == 0b00111001011110000010100101000000,"Test failed"
    assert temp.reverse_bits(0b00000000000000000000000000000000) == 0b00000000000000000000000000000000,"Test failed"
    assert temp.reverse_bits(0b11111111111111111111111111111111) == 0b11111111111111111111111111111111,"Test failed"
    assert temp.reverse_bits(0b01010101010101010101010101010101) == 0b10101010101010101010101010101010,"Test failed"
    assert temp.reverse_bits(0b10000000000000000000000000000000) == 0b00000000000000000000000000000001,"Test failed"
    assert temp.reverse_bits(0b11001010101110101111000011011011) == 0b11011001110000101110111010101011,"Test failed"
    assert temp.reverse_bits(0b00110011001100110011001100110011) == 0b11001100110011001100110011001100,"Test failed"
    assert temp.reverse_bits(0b00000000000000000000000000000001) == 0b10000000000000000000000000000000,"Test failed"
    assert temp.reverse_bits(0b01010101010101010101010101010101) == 0b10101010101010101010101010101010,"Test failed"
    assert temp.reverse_bits(0b10000000000000000000000000000010) == 0b01000000000000000000000000000001,"Test failed"

test_reverse_bits()